/**
 * tokenizer.ts — CST (Cognitive Semantic Tokenizer) for English.
 *
 * Converts raw English text → CSTToken list.
 * Self-contained: no dependencies.
 *
 * Token types:
 *   Core:       CONCEPT | ROLE | REL | LIT
 *   Structural: NEG | QUERY | COND | CAUSE | FUTURE | PAST | MODAL
 *   Question:   WHAT_Q | WHICH_Q | WHERE_Q | WHEN_Q | WHO_Q | WHY_Q | HOW_Q
 */

export type TokenType =
  | "CONCEPT"
  | "ROLE"
  | "REL"
  | "LIT"
  | "NEG"
  | "QUERY"
  | "COND"
  | "CAUSE"
  | "FUTURE"
  | "PAST"
  | "MODAL"
  | "WHAT_Q"
  | "WHICH_Q"
  | "WHERE_Q"
  | "WHEN_Q"
  | "WHO_Q"
  | "WHY_Q"
  | "HOW_Q";

export interface CSTToken {
  type: TokenType;
  value: string; // e.g. "CONCEPT:write", "ROLE:agent", "NEG"
  surface: string; // original word
  field?: string; // semantic field (for CONCEPT tokens)
  role?: string; // morphological role (for ROLE tokens)
}

