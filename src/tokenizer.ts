// ── Morphology helpers ────────────────────────────────────────────────────────

function resolveField(word: string): string | undefined {
  const w = word.toLowerCase();

  // 1. Direct lookup
  if (SEMANTIC_FIELDS[w]) return SEMANTIC_FIELDS[w];
  // 2. Silent-e restoration ("writ" → "write")
  if (SEMANTIC_FIELDS[w + "e"]) return SEMANTIC_FIELDS[w + "e"];

  // 3. Single suffix strip + silent-e
  for (const [suffix] of SUFFIX_ROLES) {
    if (w.endsWith(suffix) && w.length > suffix.length + 2) {
      const stem = w.slice(0, w.length - suffix.length);
      const f = SEMANTIC_FIELDS[stem] ?? SEMANTIC_FIELDS[stem + "e"];
      if (f) return f;

      // 4. Nested: strip a second suffix from the stem
      //    e.g. "readable" → strip "able" → "read" → field "know"
      //         "organization" → strip "tion" → "organiza" → strip "ize" → "organ" (no hit)
      //         "simplification" → strip "ification" → "simpl" → strip "ify" etc.
      for (const [suffix2] of SUFFIX_ROLES) {
        if (stem.endsWith(suffix2) && stem.length > suffix2.length + 2) {
          const stem2 = stem.slice(0, stem.length - suffix2.length);
          const f2 = SEMANTIC_FIELDS[stem2] ?? SEMANTIC_FIELDS[stem2 + "e"];
          if (f2) return f2;
        }
      }
    }
  }

  // 5. Prefix strip fallback — strip known prefix, resolve the bare stem
  //    e.g. "disorganize" → "organize" → "work"
  //         "rewrite" → "write"
  for (const prefix of Object.keys(PREFIX_ROLES)) {
    if (w.startsWith(prefix) && w.length > prefix.length + 2) {
      const stem = w.slice(prefix.length);
      const f = SEMANTIC_FIELDS[stem] ?? SEMANTIC_FIELDS[stem + "e"];
      if (f) return f;
    }
  }

  return undefined;
}

function detectRole(word: string): string | undefined {
  const w = word.toLowerCase();
  for (const [suffix, role] of SUFFIX_ROLES) {
    if (w.endsWith(suffix) && w.length > suffix.length + 2) return role;
  }
  for (const [prefix, role] of Object.entries(PREFIX_ROLES)) {
    if (w.startsWith(prefix) && w.length > prefix.length + 2) return role;
  }
  return undefined;
}

// ── Core tokenizer ────────────────────────────────────────────────────────────

/** Convert an English sentence into CST reasoning-mode tokens. */
export function tokenize(sentence: string): CSTToken[] {
  const tokens: CSTToken[] = [];
  const words = sentence.replace(/[?!.]+$/, "").split(/\s+/);

  if (sentence.trimEnd().endsWith("?")) {
    tokens.push({ type: "QUERY", value: "QUERY", surface: "?" });
  }

  // Pre-scan: mark word indices consumed by a compound bigram
  const skipIdx = new Set<number>();
  for (let i = 0; i < words.length - 1; i++) {
    const w0 = words[i].toLowerCase().replace(/^[.,;:!?"']+|[.,;:!?"']+$/g, "");
    const w1 = words[i + 1]
      .toLowerCase()
      .replace(/^[.,;:!?"']+|[.,;:!?"']+$/g, "");
    const compound = `${w0} ${w1}`;
    if (COMPOUND_FIELDS[compound]) {
      skipIdx.add(i);
      skipIdx.add(i + 1);
      tokens.push({
        type: "CONCEPT",
        value: `CONCEPT:${COMPOUND_FIELDS[compound]}`,
        surface: `${words[i]} ${words[i + 1]}`,
        field: COMPOUND_FIELDS[compound],
      });
    }
  }

  for (let idx = 0; idx < words.length; idx++) {
    if (skipIdx.has(idx)) continue;
    const word = words[idx];
    const w = word.toLowerCase().replace(/^[.,;:!?"']+|[.,;:!?"']+$/g, "");
    if (!w) continue;

    const structural = STRUCTURAL_MAP[w];
    if (structural) {
      tokens.push({ type: structural, value: structural, surface: word });
      continue;
    }

    const rel = RELATION_MAP[w];
    if (rel) {
      tokens.push({ type: "REL", value: rel, surface: word });
      continue;
    }

    if (FUNCTION_WORDS.has(w)) continue;

    const field = resolveField(w);
    const role = detectRole(w);

    if (field && role) {
      tokens.push({
        type: "CONCEPT",
        value: `CONCEPT:${field}`,
        surface: word,
        field,
      });
      tokens.push({ type: "ROLE", value: `ROLE:${role}`, surface: word, role });
    } else if (field) {
      tokens.push({
        type: "CONCEPT",
        value: `CONCEPT:${field}`,
        surface: word,
        field,
      });
    } else if (role) {
      tokens.push({ type: "ROLE", value: `ROLE:${role}`, surface: word, role });
    } else {
      tokens.push({ type: "LIT", value: `LIT:${w}`, surface: word });
    }
  }

  return tokens;
}

/** Human-readable token stream string. */
export function tokenStream(sentence: string): string {
  return tokenize(sentence)
    .map((t) => t.value)
    .join(" ");
}
